package pexe;

public class Ryce {

	private String hoqe;
	private int juma;

	public Ryce(String hoqe, int juma) {
		this.hoqe = hoqe;
		setJuma(juma);
	}
	
	public String getHoqe() {
		return hoqe;
	}
	
	public int getJuma() {
		return juma;
	}
	
	public void setJuma(int juma) {
		this.juma = juma;
	}
	
}
